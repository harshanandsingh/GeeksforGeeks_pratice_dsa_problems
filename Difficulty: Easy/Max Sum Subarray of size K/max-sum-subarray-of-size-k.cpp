class Solution {
  public:
    int maximumSumSubarray(vector<int>& arr, int k) {
        // code here
        int i=0,j=0;
        int n = arr.size();
        int sum=0,max_=0;
        while(j<n){
            sum+=arr[j];
            if(j-i+1 > k ) sum-=arr[i++];
            max_=max(max_,sum);
            j++;
        }
        return max_;
    }
};


